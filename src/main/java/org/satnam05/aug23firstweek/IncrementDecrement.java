package org.satnam05.aug23firstweek;

public class IncrementDecrement {

    public static void main(String[] args) {

        int x =7;
        ++x;
        System.out.println(x);

        int a = 2* x++;
        System.out.println(a);

        int b = 2* ++x;
        System.out.println(b);


    }
}
