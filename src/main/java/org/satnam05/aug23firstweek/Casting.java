package org.satnam05.aug23firstweek;

public class Casting {

    public static void main(String[] args) {

        double a = 9.999976;
        System.out.println(a);

        int b = (int) a;
        System.out.println(b);

        int c = (int) Math.round(a);
        System.out.println(c);

        int x = 1;
        x+=3.5;
        System.out.println(x);

    }
}
